            Il fut d'ailleurs arrêté en 1972 pour avoir joué le sketch "The seven dirty words" (Disponible dans l'onglet
            vidéo de ce site), au Milwaukee's Summerfest et chargé d'avoir enfreint la loi locale sur la diffusion de
            contenus obscènes. L'affaire fut par la suite classée "sans suite", le juge déclarant que ces propos étaient
            "certes indécents", mais n'enfreignait pas la loi, car ceux-ci ne provoquaient pas de désordre public.
            <br>
